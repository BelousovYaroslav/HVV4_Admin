    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        
        if( edtSerial.getText().isEmpty()) {
            return;
        }
        
        theApp.SetSerial( edtSerial.getText());
        
        if( radSizeSmall.isSelected())  theApp.SetProcessedDeviceType( HVV_Admin4Constants.DEVICE_SMALL);
        if( radSizeMed.isSelected())    theApp.SetProcessedDeviceType( HVV_Admin4Constants.DEVICE_MEDIUM);
        if( radSizeBig.isSelected())    theApp.SetProcessedDeviceType( HVV_Admin4Constants.DEVICE_BIG);
        
        String strTitle = theApp.m_pMainWnd.getTitle();
        strTitle = strTitle.replaceFirst( "null", theApp.GetSerial());
        theApp.m_pMainWnd.setTitle( strTitle);
        
        
        theApp.m_pMainWnd.m_pnlImpermeability.Init();
        theApp.m_pMainWnd.m_pnlImpermeability.setVisible( true);
        theApp.m_pMainWnd.m_pnlEnterSerial.setVisible( false);
    }//GEN-LAST:event_btnNextActionPerformed


