    public void RefreshTime( int nSeconds) {
        if( nSeconds == -1)
            lblStopWatch.setText( "--:--");
        else
            lblStopWatch.setText( String.format( "%02d", nSeconds / 60) + ":" + String.format( "%02d", nSeconds % 60));
    }
    
